/** The DDL the seed db and every lane share (must match src/lib/db usage). */
export const DB_STUB_SCHEMA = "CREATE TABLE IF NOT EXISTS items (id INTEGER PRIMARY KEY AUTOINCREMENT, n INTEGER);";

/** The DB-writing mission the e2e walks in parallel: open -> increment -> count is 1. */
export function dbCounterMission(id: string): { mission: Mission; actions: Action[] } {
  return {
    mission: {
      id,
      name: "increment the counter",
      description: "submit the form once; the sqlite-backed count becomes 1",
      intendedSteps: ["open the counter", "click increment"],
      acceptanceCriteria: [
        { type: "deterministic", predicate: { kind: "text-equals", selector: "[data-testid=count]", expected: "1" }, nl: "count reads 1 after one increment" },
      ],
    },
    // the walker navigates to the lane's base url first; the sequence is just the
    // interaction (a leading relative navigate would be an invalid goto target).
    actions: [
      { kind: "click", target: { strategy: "data-testid", value: "increment" } },
    ],
  };
}
