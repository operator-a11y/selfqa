/**
 * M5-F-INT — a DB-backed stub app for the end-to-end isolation gate.
 *
 * A minimal Next app whose state lives in SQLite (node:sqlite — no Prisma engine,
 * no network, zero new deps; the load-bearing property is identical: a real
 * server-side DB write routed by a RUNTIME env var to a per-lane file). It honors
 * the §9.3 RUNTIME-ENV CONTRACT: `openDb()` reads `process.env.DATABASE_URL` at
 * REQUEST time (never a build-time-inlined constant), so one shared `next build` +
 * a per-lane DATABASE_URL at `next start` actually routes each lane to its own DB.
 *
 * Deliberately uses a SERVER-component page + a native form POST (no client
 * hydration needed), so the walk is robust: navigate -> submit -> the row is
 * written -> the page server-renders the new count from THIS lane's DB.
 *
 * fixtures.snapshotRestore.kind === 'db-file-copy' is the activation switch; the
 * runner's DbRestoreIsolation restores each lane to the seed before every mission.
 */
